<br/><br/><br/><h1>Seguridad Proyecto NST</h1><br/>

<p><h2><span style="font-weight:bold">General</span></h2></p><br/>
<p>La seguridad de tu información es un tema de gran importancia para el Proyecto NST, por ello tomamos las medidas necesarias y posibles para proteger tu información personal. Recuerda que ninguna transmisión de datos o almacenamiento de información en Internet tiene garantizada la seguridad al 100%. A continuación presentamos un resumen de las medidas que toma Proyecto NST para proteger tu información.</p>
<p>Proyecto NST continúa evaluando e implementando mejoras en sus prácticas y tecnologías relacionadas con la seguridad, aunque nuestros esfuerzos sólo pueden centrarse en evitar, dentro de lo posible, el acceso no autorizado a tus datos personales.</p><br/>
<p><span style="font-weight:bold">Nuestras medidas de seguridad</span></p>
<ul><br/>
<li><span style="font-weight:bold">Encriptación y tecnología "secure sockets layer" (SSL)</span>
<p>Proyecto NST usa la tecnología de encriptación y tecnología "secure sockets layer" (SSL) al transmitir ciertos tipos de datos.  Cuando ocurren transacciones SSL, la mayoría de los navegadores muestran el ícono de un candado en la parte inferior de la ventana. Cuando Proyecto NST te pida tus datos como parte de un proceso de verificación, tu información será encriptada por SSL.  Los datos que nos proporciones se guardarán de manera segura en nuestro servidor.</p></li><br/>

<li><span style="font-weight:bold">Almacenamiento seguro</span>
<p>Proyecto NST hace todo lo posible por tener las medidas físicas, electrónicas y administrativas legalmente requeridas para proteger la información personal de sus usuarios.</p></li><br/>

<li><span style="font-weight:bold">Acceso de desarrolladores a la información</span>
<p>Sólo los empleados del Proyecto NST que deben entrar en contacto con la información personal de nuestros usuarios tienen acceso a la misma, ello porque su trabajo así lo requiere y para poder proporcionar el servicio que solicitan los usuarios.</p></li> <br/>
</ul><br/>

