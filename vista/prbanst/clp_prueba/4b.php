<h2>"La ballena y el vigía (1ª. parte)"</h2>
<br>
<div id="lapieza"><h4>
	<p>Hace muchos años, un grupo de hombres partió a cazar ballenas a los mares del Sur.</p>
	<p>Iban en un pequeño barco ballenero movido por velas.</p>
	<p>Después de muchos días de viaje, llegaron a una parte donde había muchas ballenas. Ahí
		echaron anclas.</p>
	<p>Un hombre se subió a un mástil del barco para ver si aparecían ballenas. Era el vigía.</p>
	<p>Cuatro hombres de la tripulación se embarcaron en un pequeño bote y fueron bajados al
		mar. Otros marineros se quedaron en la cu­bierta del barco.</p>
	<p>En el bote iba un encargado del timón, el timonel; dos hombres estaban a cargo de los
		remos. El último era el arponero, encargado de manejar el arpón con el que
		querían capturar a las ballenas.</p>
	<p>Apenas el bote se había alejado unos metros, el vigía vio una ballena por la parte
		delantera del barco.</p>
	<p>-¡Ballena a la vista! -gritó-. ¡Ballena a proa!</p>
	<p>Pero nadie lo oyó. Se había olvidado de usar un megáfono, que es una corneta estrecha por un
		lado y ancha por el otro. El lado estrecho se pone junto a la boca. Con el
		megáfono, la voz se hace más sonora y se puede dirigir hacia donde uno
		quiera.</p>
	<p>Los del bote no oyeron el grito de¡ vigía, y la ballena escapó sin que la vieran.</p>
	<p>Poco después, la misma ballena apareció por la parte trasera del barco.</p>
	<p>-¡Ballena a la vista! -gritó otra vez el vigía. -¡Ballena a popa!</p>
	<p>Gracias al megáfono, los hombres del bote oyeron la voz del vi­gía. El timonel dirigió el
		bote hacia la popa; los remeros movieron los remos con todas sus fuerzas y el
		arponero se preparó para lan­zar su arpón. Pero la simpática ballena, cuyo oído
		era excelente , también había escuchado el grito y ¡Plaf! se escondió debajo
		del agua donde nadie podía capturarla.</p>
</h4>
</div>
