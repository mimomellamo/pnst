<h1>Cookies</h1>
<p>Una cookie es una pequeña cantidad de información que generalmente incluye un identificador anónimo único, la cual ciertos lugares de Internet envían a tu navegador para que se guarde en el disco duro de tu computadora.</p>
<p>Cada sitio Web puede enviar una cookie propia a tu navegador, si las preferencias de éste lo permiten; pero para proteger tu privacidad, los navegadores sólo autorizan que los sitios Web ingresen a las cookies que ellos mismos mandaron y no a aquellas enviadas por otros.</p>
<p><span style="font-weight:bold">Opciones respecto a las Cookies</span></p>

<ul >
<li>Puedes configurar tu navegador para que acepte todas las cookies que te envían, para que las rechace todas o para que se le avise en el momento en que una está siendo guardada en el disco duro de tu computadora. Cada navegador es distinto, así que por favor revisa el menú de ayuda del que utilizas para ajustar sus preferencias respecto a las cookies.</li>
<li>Si optas por rechazar todas las cookies, no podrás utilizar el servicio PNST y tampoco podrás utilizar al máximo algunas opciones. Sin embargo, también puedes navegar por sitios que no requieren que aceptes las cookies.</li>
</ul>
<p><span style="font-weight:bold">Prácticas de PNST respecto a las cookies</span></p>
<p>PNST utiliza cookies para diversos fines:</p>
<ul >
<li>Conservar las preferencias que especificaste cuando usas el servicio de PNST.</li>
<li>Proporcionar reportes generales estadísticos internos y para nuestros clientes.</li>
<li>Hacer estudios para mejorar el contenido y los servicios de Yahoo!.</li>
<li>Apoyar las medidas de seguridad como la de solicitar que debas rescribir tu contraseña de PNST después de cierto tiempo para protegerte de que otras personas no autorizadas ingresen en tu cuenta.</li>
<li>Ayudar en la identificación de actividades que puedan ser fraudulentas.</li>
</ul>

<p>Esta página describe las prácticas actuales de PNST con respecto a este servicio en particular. Esta información puede cambiar después de que PNST modifique este servicio, quitando o añadiendo características al mismo. Para saber cómo usa PNST los datos personales que le proporcionas, por favor mira la <a href="?controlador=Index&accion=polSeguridad" >Política de privacidad PNST</a>.</p><br/>
