<br/><br/><br/><h1>Almacenamiento de información</h1>

<br/><p><span style="font-weight:bold">Información de cuenta</span></p>
<ul>
<li>Cuando te registras en Proyecto NST, o nos envías información, nosotros creamos una copia temporal de tus datos para impedir que éstos se pierdan en caso de un error humano o alguna falla electrónica durante el registro.</li>
<li>Tu información de cuenta se conserva en Proyecto NST (en nuestra base de datos de usuarios).</li>
<li>Si pidieras a Proyecto NST que elimine tu cuenta, ésta –en la mayoría de los casos– será primero desactivada y a continuación borrada de nuestra base de datos en aproximadamente 20 días. La eliminación tarda ese periodo para prevenir perdida de información que involucrase a otros usuarios.</li>
<li>Recuerda que cualquier información que nosotros tenemos de tu registro, es conservada un tiempo posterior a tu solicitud de renuncia, aunque haya desaparecido de nuestra base de datos de usuarios activos.</li>
</ul><br/>
<p><span style="font-weight:bold">Servidores</span></p>
<ul>
<li>El computador de Proyecto NST (llamadas servidor), que te envía sus páginas Web, procesan y almacenan una cantidad enorme de información cada día; las actividades de estas máquinas quedan registradas en los "registros de actividad".</li>
<li>Los registros de actividad se usan para diversas funciones: análisis, investigación, etc.; las cuales fueron descritas anteriormente. Una vez que se utiliza dicha información, se almacena en un lugar inaccesible. Antes de que llegue a ese punto, tu Nombre de usuario (ID) de Proyecto NST permanece en los registros de actividad de nuestro servidor activo.</li>
</ul>
