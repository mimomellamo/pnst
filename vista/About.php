<br/><br/><br/><h1>Acerca del Proyecto Educativo NST</h1><br/>
				
<h2>El Proyecto NST (aplicación para medir la comprensión lectora de los alumnos de 1° a 4° año básico) tiene <i>dos objetivos fundamentales</i>:</h2><br/>
		
<h3><b>1.</b> Queremos <b>ayudar a los profesores en la tarea de medir la comprensión lectora en etapas iniciales en la educación escolar primaria</b>.</h3>

<p>Construir un instrumento de medición web que permita a los profesores de la asignatura de Lenguaje y Comunicación (1º a 4º básico) acortar los tiempos de aplicación y revisión de pruebas, otorgando información estadística, en formato digital, sobre los progresos de los alumnos.
</p>

<p>Para lograrlo, queremos <b>promover la medición inicial de la comprensión lectora en los cursos de 1º a 4º básico.
</b>: desde la Web, disminuir los tiempos de revisión por parte de los profesores de la asignatura facilitando la aplicación de pruebas a los alumnos familiarizando a los alumnos y profesores con las tecnologías.</p><br/>

<h3><b>2.</b>Queremos <b>Entregar información estadística sobre los resultados de cada alumno.</b>. </h3>

<p>Crear un registro histórico de los resultados de cada alumno mostrando sus progresos (comparando con las pruebas anteriores).</p>

<p>Parte de la solución está en hacer que el proceso de realización de las pruebas tengan el mínimo costo posible para el establecimiento educacional y en horas de trabajo para los profesores. En el Proyecto NST, puedes fijar las fechas de las pruebas, indicar el formato de la prueba (A ó B), mantener una lista de alumnos los cuales serán medidos y su información almacenada. La información obtenida de las respuestas de los alumnos es procesada, tabulada y graficada para un fácil entendimiento y puedas tomar la mejor decisión para mejorar la comprensión lectora de los alumnos.</p>
		
